package begginer;

import java.util.*;

public class Q4101 {

	//https://www.acmicpc.net/problem/4101
	
	//두수
	//끝 0 0
	
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		while(true)	 {
			int v1 = sc.nextInt();
			
			if(v1 == 0) return;
			
			int v2 = sc.nextInt();
			
			if(v1 > v2) {
				System.out.println("Yes");
			}else {
				System.out.println("No");
			}
		}
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
